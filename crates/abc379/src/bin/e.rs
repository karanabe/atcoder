use num::BigInt;
use num::Zero;

fn main() {
    input! {
        _n: usize,
        s: String,
    }

    let digits: Vec<u32> = s.chars().map(|c| c.to_digit(10).unwrap()).collect();

    let mut ans = BigInt::zero();
    let mut previous_sum = BigInt::zero();

    for (i, &digit) in digits.iter().enumerate() {
        let term = BigInt::from(digit) * (i as u64 + 1);

        previous_sum = previous_sum * 10u32 + term;

        ans += &previous_sum;
    }

    println!("{}", ans);
}
